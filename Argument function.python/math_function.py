# Maths Functions in Python 

import math

print (math.sqrt(4))
print (math.ceil(1.55555)) # next value 2
print (math.floor(1.55555)) # first value 1

# 5! => 1*2*3*4*5
print(math.factorial(5))

print(math.fabs(-5)) # neg or pos only show postive value
print(math.pow(2,3)) # 2*2 = 4 => 4*2 = 8
print(math.log2(2))  # log2 oda value
print(math.log10(2)) # log10 oda value
print(math.pi) # pi na constant value
print(math.e) 