# Assignment opeators

'''
 =  Assignment
+=  Addition
-=  Subtraction
*=  Multiplication
/=  Division
%=  Modulus
**= Exponentiontion
//= Floor divison
'''

a = 125
print(a)
a += 5   # a=a+5
print(a)
a -= 5
print(a) # a=a-10
a *= 5
print(a)
a /= 10
print(a)
a %= 20
print(a)
a //= 10
print(a)
a **= 20
